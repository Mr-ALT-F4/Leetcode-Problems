// calculates x raised to the power n (i.e., xn).
#include<bits/stdc++.h>

//solution---------------------------


class Solution {
public:
    double myPow(double x, int n) {
        
        double ans = pow(x,n);
        return ans;
        
    }
};

// Input: x = 2.00000, n = 10
// Output: 1024.00000


// Input: x = 2.10000, n = 3
// Output: 9.26100